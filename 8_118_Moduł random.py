#moduł random
# generowanie wartości losowych

import random # import funkcji random
print("One random number:",random.randint(1,100)) #funkcja losuje liczbę całkowitą z zakresu 1 <= N <= 100
print("\n")

print("Choosing random numer from a range",random.choice(range(1,100))) # funkcja random.choice(lista) z dostępnej listy wybierze jedną wartość
print("\n")

print("Choosing random numer from a range - easier",random.randrange(1,100)) # funkcja random.randrange(zakres liczbowy) robi to samo co random.choice(range(zakres liczbowy)) z dostępnej listy wybierze jedną wartość
print("\n")

list=['John','Ann','Peter','Susan','Emily','Greg','Chris']
random.shuffle(list)                                        #przetasowanie (wymieszanie) listy losowo
print("Recordered list:",list)
print("\n")

print("Just a random float",random.random()) #random.random() losuje liczbę typu float w zakresie od 0-1
print("\n")







