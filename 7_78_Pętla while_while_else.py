#inkrementacja

i=1 #- zmienna która będzie regulować ilość powtórzeń, pokazuje który raz jest wykonywana czynność
imax = 10#- ilość powtórzeń 

while i<= imax :#- sprawdzenie czy liczb i będzie mniejsza bądź równa imax,
    #-puki warunek będzie spełniony wykonuj w pętli
    #- pamiętaj o dwukropku
    print(i, "I like Python")
    i+=1 # inkrementacja
else: #- w przeciwnym wypadku, gdy kończy się pętla while
    print("Now i =",i)

