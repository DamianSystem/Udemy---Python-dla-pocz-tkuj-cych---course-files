#trochę matematyki w Python

f_smaller = 3.141592653589793 # f od typu liczb float
f_bigger  = 3.87756392764 # uwaga zapis liczby jest z kropką


print(f_smaller, f_bigger)
print("\n")


print(int(f_smaller), int(f_bigger)) # rzutowanie (zapis) w typie int liczb wcześniej zdefiniowanych(obcina części dziesiętne, zostawia całkowite)
print("\n")                          # wynik zapisu to: 3 3


print(abs(f_smaller), abs(-f_smaller)) # wartość bezwzględna z liczby (abs(liczba)) zwracana wartość jest dodatnia
print("\n")                            # abs(-f_smaller) można dopisywać minus przy liczbie zmieniając ją na przeciwną


print(round(f_smaller,2), round(f_bigger,2), round(f_bigger,3)) # zaokrąglenie (round) jeżeli wartość będzie x>=5 zaokrąglenie będzie w góre, jeżeli x<5 zaokrąglenie będzie w dół.
print("\n") # round(f_smaller,2) - zaokrąglenie f_smaller do dwóch miejsc po przecinku.


print(min(f_smaller, f_bigger), max(f_smaller, f_bigger)) # funkcja min(liczby) podaje mniejszą liczbę  ze zbioru liczb
print("\n")                                               # funkcja max(loczby) podaje większą liczbę ze zbioru liczb

list=[1,2,3,4,5,4,4,5,4]

# wyliczeni średnij elementów z listy

