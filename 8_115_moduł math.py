#moduł math

f_smaller = 3.141592653589793 # f od typu liczb float
f_bigger  = 3.87756392764 # uwaga zapis liczby jest z kropką


import math # import funkcji math

print(math.ceil(f_smaller), math.ceil(f_bigger))# math.ceil zwraca najmniejszą liczbę całkowitą, która jest większa od przekazanego argumentu( tu liczba 4)
print("\n")

print(math.floor(f_smaller), math.floor(f_bigger))# math.floor zwraca największą liczbę całkowitą, która jest mniejsza od przekazanego argumentu( tu liczba 3)
print("\n")


print(math.ceil(-f_smaller), math.ceil(-f_bigger))# math.ceil zwraca najmniejszą liczbę całkowitą, która jest większa od przekazanego argumentu( tu liczba -3)
print("\n")

print(math.floor(-f_smaller), math.floor(-f_bigger))# math.floor zwraca największą liczbę całkowitą, która jest mniejsza od przekazanego argumentu( tu liczba -4)
print("\n")

print(pow(2,8),pow(9,0.5))# pow(liczba, potęga liczby) - potęgowanie (od power)
print("\n")

print(math.sqrt(16))#math.sqrt(liczba) -pierwiastek kwadratowy z liczby
print("\n")

print(math.pi, math.e)# wywołanie wartości w module stałych 'pi'(3.14...) i 'e'(2,71...)
print("\n")

print(math.sin(math.pi/2), math.cos(math.pi/4))# obliczanie sinusa (math.sin) i cosinusa (math.cos) ze zmiennych
print("\n")





