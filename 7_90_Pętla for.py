# Pętla FOR

# każdy element z listy musi zostać przetworzony
# dla każdego elementu z listy stało się coś
# pętla wykonuje się do ostatniego elementu z listy
# zadanie utworzyć adres email dla każdej z tych osób

domain = 'mycompany.com'



persons = ['Elizabeth','Steven','Sebastian','Margaret','Svetlana','Raphael']


for person in persons:    # składnia for następnie jakaś nazwa(person) następnie in następnie lista z której należy brać te osoby (persons)
    #for zmienna używana w opętli po wyrazie in pobierana z listy persons (od pierwszego wyrazu z listy do ostatniego)
   
    email = person + '@' + domain
    print('Email for\t', person, '\tis\t', email)
else: # wykonuje się instrukcje po wykonaniu wszystkich elementów z pentli for
    print('--end of the list --')
    


