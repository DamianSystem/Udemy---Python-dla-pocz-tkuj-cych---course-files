#Instrukcja warunkowa If

age = 20

if age >=18: #warunek logiczny jeżeli, pamiętaj o dwukropku
    print("You are adult and you can buy alcohol")
else: #w przeciwnym wypadku, pamiętaj o dwukropku
    print("You are too young to buy alcohol")

isDrunk = False

if age >=18 and not isDrunk: #warunek logiczny jeżeli, pamiętaj o dwukropku
    print("You are adult and you can buy alcohol")
else: #w przeciwnym wypadku, pamiętaj o dwukropku
    print("Sorry, we cannot sell you alcohol")

isRestrictedArea = False

if age >=18 and not isDrunk and not isRestrictedArea: #- warunek logiczny jeżeli, pamiętaj o dwukropku
    print("You are adult and you can buy alcohol") #- pamiętaj o wcięciach
else: #- w przeciwnym wypadku, pamiętaj o dwukropku
    print("Sorry, we cannot sell you alcohol") #- pamiętaj o wcięciach








