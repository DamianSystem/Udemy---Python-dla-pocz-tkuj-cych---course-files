message = 'Document "cv.doc" was printed on printer: XEROX'
print(message.find(':')) # - znalezienie dwukropka na pozycji 40 w powyższym tekście

print(message[message.find(':')+2:]) # - wydobycie wartości XEROX - odsunięcie o 2 wartości (dwukropek i spcje)

print(message[message.find('"')+1:]) # - wydobycie wartości cv.doc kurs
tmp = message[message.find('"')+1:] # - wydobycie wartości cv.doc kurs
print(tmp[:tmp.find('"')]) # - wydobycie wartości cv.doc kurs

print(message[message.find(' "')+2:message.find('" ')]) # - wydobycie wartości cv.doc moje rozwiązanie





