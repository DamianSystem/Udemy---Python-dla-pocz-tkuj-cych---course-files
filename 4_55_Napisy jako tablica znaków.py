s="A Pythonascript"
print(s[0]) # - wyświetlenie pierszgo elementu (w python nr 0) tablicy, wynik  literka A
print(s[2]) # - wyświetlenie trzeciego elementu (w python nr 2) tablicy, wynik  literka P
# - uwaga na zakresy
print(s[2:7]) # - wyświetlenie zakresu od trzeciego do siudmego elementu (w python nr 2 do 7 - bez 7 elementu ) tablicy, wynik  - Pytho

print(s[2:8]) # - wyświetlenie zakresu od trzeciego do dziewiątego elementu (w python nr 2 do 8 - bez 8 elementu) tablicy, wynik  - Python

print(s[:8]) # - wyświetlenie zakresu do dziewiątego elementu (w python do 8 - bez 8 elementu) tablicy, wynik  - A Python

print(s[8:]) # - wyświetlenie zakresu od dziewiątego elementu (w python od 8 z 8 elementem) tablicy, wynik  - ascript

print(s[2:999]) #- Python wyświetli elemt od trzeciego elemntu (w python od 2 z 2 elementem) tablicy do końca naszej tablicy ponieważ zawiera mniej elementów.

