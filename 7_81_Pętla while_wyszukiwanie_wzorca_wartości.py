max = len(values) -2#- zmienna max wyliczy ilość liczb w liście
print(max)

while i<max: #- testowo sprawdzamy czy pętla while dobrze nam działa
    print(i,values[i])
    if(values[i]<values[i+1] and values[i+1]<values[i+2]):
        print('\tFound: ', values[i], values[i+1], values[i+2])

    i+=1


